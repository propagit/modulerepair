                     <h3>
                     Wave1 has a long history of providing high quality reliable wireless data and voice communications solutions to corporate clients, health, education and government departments, system integrators and reseller partners.</h3>
					<p>
In 1985 with humble beginnings as a computer & network component reseller & service repair centre, the company realised there was a requirement for alternatives to traditional building to building costly connectivity.  Being a family owned business and starting with one office in the outer suburbs of Melbourne the owners took the dive into solely Wireless Communications.<br /><br />  

In 1988 ADE Network Technology as it was known then installed the first Licenced Ethernet Microwave in Australia and from there became the leading Wireless Communications Company in Australia.  Wayne and Bill Anderson together with their highly experienced and knowledgeable staff were becoming widely recognised in the Telecommunications and Information Technology industries as innovators in their field.<br /><br />  

In late 2000 after much debate the family decided to allow ADE Network Technology to be acquired by WaveRider Communications Inc of Canada.  WaveRider were manufacturing 900MHz wireless bridges and were a leading organisation based in Toronto.  5 Years later in 2006 Wayne Anderson re-acquired the business and quickly changed its name from Canada's WaveRider to Australia's Wave1. <br /><br /> 

Number 1 for Speed, in microwave connectivity.  Number 1 for Service, with expert Sales Staff & Engineers where the right solution for each application is always determined.  Number 1 for Reliability, stringently tested equipment for reliable connectivity all the time. <br /><br />  


With Offices in Melbourne, Sydney and Brisbane and close reseller partners in Adelaide, Perth & Tasmania Wave1 are available to provide a turnkey solution every time.   

